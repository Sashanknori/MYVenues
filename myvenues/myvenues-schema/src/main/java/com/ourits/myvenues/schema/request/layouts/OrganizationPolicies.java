package com.ourits.myvenues.schema.request.layouts;

public class OrganizationPolicies {

	private String constraintDescription;
	private String constraintType;
	private Integer constraintId;

	public String getConstraintDescription() {
		return constraintDescription;
	}

	public void setConstraintDescription(String constraintDescription) {
		this.constraintDescription = constraintDescription;
	}

	public String getConstraintType() {
		return constraintType;
	}

	public void setConstraintType(String constraintType) {
		this.constraintType = constraintType;
	}

	public Integer getConstraintId() {
		return constraintId;
	}

	public void setConstraintId(Integer constraintId) {
		this.constraintId = constraintId;
	}

}
